    protected void tablesiswa() {
        Object[] Data = {"NIS", "Nama Siswa", "No Telepon", "Tempat Lahir", "Tanggal Lahir", "Jenis Kelamin", "Agama", "Jurusan", "Alamat"};
        tabmode = new DefaultTableModel(null, Data);
        datatablesiswa.setModel(tabmode);
        String sql = "SELECT * FROM datasiswa";
        try {
            
            java.sql.Statement stat = conn.createStatement();
            ResultSet datasiswa = stat.executeQuery(sql);
            while(datasiswa.next()) {
                String nissiswa = datasiswa.getString("nis");
                String namasiswa = datasiswa.getString("nama");
                String notelpsiswa = datasiswa.getString("notelp");
                String tmptlahirsiswa = datasiswa.getString("tempatlahir");
                String tgllahirsiswa = datasiswa.getString("tgllahir");
                String jksiswa = datasiswa.getString("jk");
                String agamasiswa = datasiswa.getString("agama");
                String jurusansiswa = datasiswa.getString("jurusan");
                String alamatsiswa = datasiswa.getString("alamat");

                String[] data = {nissiswa, namasiswa, notelpsiswa, tmptlahirsiswa, tgllahirsiswa, jksiswa, agamasiswa, jurusansiswa, alamatsiswa};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            
        }
    }
    
