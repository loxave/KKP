    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO datasiswa VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
        
        if(txtnis.getText().trim().isEmpty() && txtnamalengkap.getText().trim().isEmpty() &&
            txtnotelepon.getText().trim().isEmpty()) {
            
            warnnis.setText("NIS Wajib Diisi!");
            warnnama.setText("Nama Wajib Diisi!");
            warnnotelp.setText("No Telepon Wajib Diisi!");
            
        } else if(txtnis.getText().trim().isEmpty()){
            warnnis.setText("NIS Wajib Diisi!");
        } else if(txtnamalengkap.getText().trim().isEmpty()){
            warnnama.setText("Nama Wajib Diisi!");
        } else if(txtnotelepon.getText().trim().isEmpty()){
            warnnotelp.setText("No Telepon Wajib Diisi!");
        } else
        
        try {
            PreparedStatement datasiswa = conn.prepareStatement(sql);
            datasiswa.setString(1, txtnis.getText());
            datasiswa.setString(2, txtnamalengkap.getText());
            datasiswa.setString(3, txtnotelepon.getText());
            datasiswa.setString(4, txttempat.getText());
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(datetgllahir.getDate());
            datasiswa.setString(5, date);
            
            String jkel = "";
            if(rblaki.isSelected()) jkel = "Laki - laki";
            else jkel = "Perempuan";
            datasiswa.setString(6, jkel);
            
            datasiswa.setString(7, cbxagama.getSelectedItem().toString());
            datasiswa.setString(8, cbxjurusan.getSelectedItem().toString());
            datasiswa.setString(9, txtareaalamat.getText());
            
            datasiswa.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Siswa Berhasil Disimpan");
            kosong();
            txtnis.requestFocus();
            tablesiswa();
            
            } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Siswa Gagal Disimpan");
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

