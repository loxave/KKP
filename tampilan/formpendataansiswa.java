    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Hapus Data Siswa ?", "Menghapus Data", JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok == 0 ) {
            String sql = "DELETE FROM datasiswa WHERE nis='"+txtnis.getText()+"'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Siswa Dihapus");
                kosong();
                txtnis.requestFocus();
                tablesiswa();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Siswa Gagal Dihapus"+e);
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

