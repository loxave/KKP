    private void datatablesiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_datatablesiswaMouseClicked
        // TODO add your handling code here:
        
        try {
            tabmode = (DefaultTableModel)datatablesiswa.getModel();
            
            int bar = datatablesiswa.getSelectedRow();
            String nis = tabmode.getValueAt(bar, 0).toString();
            String nama = tabmode.getValueAt(bar, 1).toString();
            String notelp = tabmode.getValueAt(bar, 2).toString();
            String tmptlahir = tabmode.getValueAt(bar, 3).toString();
            
            java.util.Date tgllahir = new SimpleDateFormat("yyyy-MM-dd").parse((String)tabmode.getValueAt(bar, 4));
            
            String jk = tabmode.getValueAt(bar, 5).toString();
            String agama = tabmode.getValueAt(bar, 6).toString();
            String jurusan = tabmode.getValueAt(bar, 7).toString();
            String alamat = tabmode.getValueAt(bar, 8).toString();

            txtnis.setText(nis);
            txtnis.setEditable(false);
            
            txtnamalengkap.setText(nama);
            txtnotelepon.setText(notelp);
            txttempat.setText(tmptlahir);
            datetgllahir.setDate(tgllahir);
        
            if(jk.equals("Laki - laki")) {
                rblaki.setSelected(true); 
                rbperempuan.setSelected(false);
            } else {
                rblaki.setSelected(false); 
                rbperempuan.setSelected(true);
            }

            cbxagama.setSelectedItem(agama);
            cbxjurusan.setSelectedItem(jurusan);
            txtareaalamat.setText(alamat);
        } catch (ParseException ex) {
            Logger.getLogger(formpendataansiswa.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_datatablesiswaMouseClicked

