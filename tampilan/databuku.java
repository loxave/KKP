    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Hapus", "Konfirmasi Dialog", JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok == 0 ) {
            String sql = "DELETE FROM databuku WHERE kode_buku='"+txtkodebuku.getText()+"'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Buku Berhasil Dihapus");
                kosong();
                txtkodebuku.requestFocus();
                table_databuku();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Buku Gagal Dihapus"+e);
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

