    private void tabledatabukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabledatabukuMouseClicked
        // TODO add your handling code here:
        int bar = tabledatabuku.getSelectedRow();
        String kodebuku = tabmode.getValueAt(bar, 0).toString();
        String judulbuku = tabmode.getValueAt(bar, 1).toString();
        String mapel = tabmode.getValueAt(bar, 2).toString();
        String tipebuku = tabmode.getValueAt(bar, 3).toString();
        String semester = tabmode.getValueAt(bar, 4).toString();
        
        txtkodebuku.setText(kodebuku);
        txtkodebuku.setEditable(false);
        
        txtjudulbuku.setText(judulbuku);
        txtmapel.setText(mapel);
        
        if(tipebuku.equals("Paket")) {
            rbpaket.setSelected(true); 
            rblks.setSelected(false);
        } else {
            rbpaket.setSelected(false); 
            rblks.setSelected(true);
        }
        
        if(semester.equals("1")) {
            rbsmt1.setSelected(true); 
            rbsmt2.setSelected(false);
        } else {
            rbsmt1.setSelected(false); 
            rbsmt2.setSelected(true);
        }
        
    }//GEN-LAST:event_tabledatabukuMouseClicked

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE databuku SET judul_buku=?, mata_pelajaran=?, tipe_buku=?, semester=? WHERE kode_buku=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            
            stat.setString(1, txtjudulbuku.getText());
            stat.setString(2, txtmapel.getText());
            
            String tipebuku="";
            if(rbpaket.isSelected()) tipebuku="Paket";
            else tipebuku = "LKS";
            stat.setString(3, tipebuku);
            
            String semester="";
            if(rbsmt1.isSelected()) semester="1";
            else semester = "2";
            stat.setString(4, semester);
            stat.setString(5, txtkodebuku.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Buku Berhasil Diubah");
            kosong();
            txtkodebuku.requestFocus();
            table_databuku();
            
            
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Buku Gagal Diubah"+e);
        }
    }//GEN-LAST:event_btneditActionPerformed

