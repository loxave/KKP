    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO databuku VALUES (?, ?, ?, ?, ?)";
        
        try {
            PreparedStatement databuku = conn.prepareStatement(sql);
            databuku.setString(1, txtkodebuku.getText());
            databuku.setString(2, txtjudulbuku.getText());
            databuku.setString(3, txtmapel.getText());
            
            String tipe = "";
            if(rbpaket.isSelected()) tipe = "Paket";
            else tipe = "LKS";
            databuku.setString(4, tipe);
            
            String smstr = "";
            if(rbsmt1.isSelected()) smstr = "1";
            else smstr = "2";
            databuku.setString(5, smstr);
            
            databuku.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Buku Berhasil Disimpan");
            kosong();
            txtkodebuku.requestFocus();
            table_databuku();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal Menambahkan Data Buku" +e);
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

