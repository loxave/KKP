    protected void table_databuku() {
        Object[] Baris = {"Kode Buku", "Judul Buku", "Mata Pelajaran", "Tipe Buku", "Semester"};
        tabmode = new DefaultTableModel(null, Baris);
        tabledatabuku.setModel(tabmode);
        String sql = "SELECT * FROM databuku";
        
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet tablebuku = stat.executeQuery(sql);
            while(tablebuku.next()) {
                String kode = tablebuku.getString("kode_buku");
                String judul = tablebuku.getString("judul_buku");
                String mapel = tablebuku.getString("mata_pelajaran");
                String tipebuku = tablebuku.getString("tipe_buku");
                String semester = tablebuku.getString("semester");
                
                String[] data = {kode, judul, mapel, tipebuku, semester};
                tabmode.addRow(data);
            }
            
        } catch (Exception e) {
            
        }
    }
