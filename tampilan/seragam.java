    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO seragam VALUES (?, ?, ?, ?, ?, ?)";
        
        try {
            PreparedStatement seragam = conn.prepareStatement(sql);
            seragam.setString(1, txtkp.getText());
            seragam.setString(2, txtnis.getText());
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(tglpem.getDate());
            seragam.setString(3, date);
            
            seragam.setString(4, cbxjs.getSelectedItem().toString());
            seragam.setString(5, txtharga.getText());
            seragam.setString(6, cbxadmin.getSelectedItem().toString());
            
            seragam.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Seragam Berhasil Disimpan");
            kosong();
            txtnis.requestFocus();
            datatable();
            rng_kode();
            
            } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Seragam Gagal Disimpan" +e);
        }
        
    }//GEN-LAST:event_btnsimpanActionPerformed

