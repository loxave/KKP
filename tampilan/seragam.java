    private void tabelseragamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelseragamMouseClicked
        // TODO add your handling code here:
        try {
            tabmode = (DefaultTableModel)tabelseragam.getModel();
            int bar = tabelseragam.getSelectedRow();
            String a = tabmode.getValueAt(bar, 0).toString();
            String b = tabmode.getValueAt(bar, 1).toString();

            java.util.Date c = new SimpleDateFormat("yyyy-MM-dd").parse((String)tabmode.getValueAt(bar, 2));
            String d = tabmode.getValueAt(bar, 3).toString();
            String e = tabmode.getValueAt(bar, 4).toString();
            String f = tabmode.getValueAt(bar, 5).toString();

            txtkp.setText(a);
            txtnis.setText(b);
            tglpem.setDate(c);
            cbxjs.setSelectedItem(d);
            txtharga.setText(e);
            cbxadmin.setSelectedItem(f);

        } catch (ParseException ex) {
            Logger.getLogger(seragam.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_tabelseragamMouseClicked

