    protected void datatable(){
        Object[] Baris ={"Kode Pembayaran", "NIS", "Tanggal Pembayaran", "Jenis Seragam", "Harga", "namaadmin"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelseragam.setModel(tabmode);
        String sql = "select * from seragam";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("kodepembayaran");
                String b = hasil.getString("nis");
                String c = hasil.getString("tglpembayaran");
                String d = hasil.getString("jenisseragam");
                String e = hasil.getString("harga");
                String f = hasil.getString("namaadmin");
                
                String[] data={a,b,c,d,e,f};
                tabmode.addRow(data);
                }
            } catch (Exception e){
            }
        
    }
