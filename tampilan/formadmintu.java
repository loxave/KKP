    protected void datatable(){
        Object[] Baris = {"ID Admin", "Nama", "Tempat", "Tanggal Lahir", "Jenis Kelamin", "No Telepon", "E-mail", "Alamat"};
        tabmode = new DefaultTableModel(null, Baris);
        tabeladmintu.setModel(tabmode);
        String sql = "SELECT id_admin, nama, tempat, tgl_lahir, jenis_kelamin, no_telpon, email, alamat FROM admintu";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("id_admin");
                String b = hasil.getString("nama");
                String c = hasil.getString("tempat");
                String d = hasil.getString("tgl_lahir");
                String e = hasil.getString("jenis_kelamin");
                String f = hasil.getString("no_telpon");
                String g = hasil.getString("email");
                String h = hasil.getString("alamat");
                
                String[] data = {a, b, c, d, e, f, g, h};
                tabmode.addRow(data);
                }
            } catch (Exception e){
            }
    }
    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        
        String sql = "insert into admintu values (?, ?, ?, ?, ?, ?, ?, ?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtidadmin.getText());
            stat.setString(2, txtnama.getText());
            stat.setString(3, txttempat.getText());
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(tgllahir.getDate());
            stat.setString(4, date);
            
            String jenis_kelamin="";
            if(rblakilaki.isSelected()) jenis_kelamin="Laki-laki";
            else jenis_kelamin="Perempuan";
            
            stat.setString(5, jenis_kelamin);
            stat.setString(6, txtnotelpon.getText());
            stat.setString(7, txtemail.getText());
            stat.setString(8, txtareaalamat.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Admin Berhasil Disimpan");
            bersihkan();
            txtidadmin.requestFocus();
            datatable();
            
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Admin Gagal Disimpan"+e);
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE admintu SET nama=?, tempat=?, tgl_lahir=?, jenis_kelamin=?, no_telpon=?, email=?, alamat=? WHERE id_admin=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            
            stat.setString(1, txtnama.getText());
            stat.setString(2, txttempat.getText());
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(tgllahir.getDate());
            stat.setString(3, date);
            
            String jenis_kelamin="";
            if(rblakilaki.isSelected()) jenis_kelamin = "Laki-laki";
            else jenis_kelamin = "Perempuan";
            stat.setString(4, jenis_kelamin);
            
            stat.setString(5, txtnotelpon.getText());
            stat.setString(6, txtemail.getText());
            stat.setString(7, txtareaalamat.getText());
            stat.setString(8, txtidadmin.getText());
            
            stat.executeUpdate();
            txtidadmin.requestFocus();
            JOptionPane.showMessageDialog(null, "Data Admin Berhasil Diubah");
            bersihkan();
            datatable();
            
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Admin Gagal Diubah"+e);
        }
    }//GEN-LAST:event_btneditActionPerformed

