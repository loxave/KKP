    private void tabeladmintuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabeladmintuMouseClicked
        // TODO add your handling code here:
        try {
            int bar = tabeladmintu.getSelectedRow();
            String a = tabmode.getValueAt(bar, 0).toString();
            String b = tabmode.getValueAt(bar, 1).toString();
            String c = tabmode.getValueAt(bar, 2).toString();

            java.util.Date d = new SimpleDateFormat("yyyy-MM-dd").parse((String)tabmode.getValueAt(bar, 3));

            String e = tabmode.getValueAt(bar, 4).toString();
            String f = tabmode.getValueAt(bar, 5).toString();
            String g = tabmode.getValueAt(bar, 6).toString();
            String h = tabmode.getValueAt(bar, 7).toString();

            txtidadmin.setText(a);
            txtnama.setText(b);
            txttempat.setText(c);
            tgllahir.setDate(d);

            if(e.equals("Laki-laki")) {
                rblakilaki.setSelected(true); 
                rbperempuan.setSelected(false);
            } else {
                rblakilaki.setSelected(false); 
                rbperempuan.setSelected(true);
            }

            txtnotelpon.setText(f);
            txtemail.setText(g);
            txtareaalamat.setText(h);
        
        } catch (ParseException ex) {
            Logger.getLogger(formadmintu.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_tabeladmintuMouseClicked

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null,"hapus","Konfirmasi Dialog", JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok==0){
            String sql = "delete from admintu where id_admin ='"+txtidadmin.getText()+"'";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Admin berhasil dihapus");
                bersihkan();
                txtidadmin.requestFocus();
                datatable();
            }catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Admin gagal dihapus" +e);
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

