    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO dataguru VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtidguru.getText());
            stat.setString(2, txtnama.getText());
            stat.setString(3, txtmapel.getText());
            stat.setString(4, txtnotelp.getText());
            stat.setString(5, txtemail.getText());
            
            String jeniskelamin="";
            if(rblaki.isSelected()) jeniskelamin="Laki-laki";
            else jeniskelamin="Perempuan";
            stat.setString(6, jeniskelamin);
            
            stat.setString(7, txttempat.getText());
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(tgllahir.getDate());
            stat.setString(8, date);
            
           
            stat.setString(9, txtareaalamat.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Guru Berhasil Disimpan");
            kosong();
            txtidguru.requestFocus();
            datatable();
            
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Guru Gagal Disimpan"+e);
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

