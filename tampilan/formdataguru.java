    private void tableguruMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableguruMouseClicked
        // TODO add your handling code here:
        try {
            int bar = tableguru.getSelectedRow();
            String a = tabmode.getValueAt(bar, 0).toString();
            String b = tabmode.getValueAt(bar, 1).toString();
            String c = tabmode.getValueAt(bar, 2).toString();
            String d = tabmode.getValueAt(bar, 3).toString();
            String e = tabmode.getValueAt(bar, 4).toString();
            String f = tabmode.getValueAt(bar, 5).toString();
            String g = tabmode.getValueAt(bar, 6).toString();

            java.util.Date h = new SimpleDateFormat("yyyy-MM-dd").parse((String)tabmode.getValueAt(bar, 7));
            String i = tabmode.getValueAt(bar, 8).toString();

            txtidguru.setText(a);
            txtnama.setText(b);
            txtmapel.setText(c);
            txtnotelp.setText(d);
            txtemail.setText(e);
            
            if(f.equals("Laki-laki")) {
                rblaki.setSelected(true); 
                rbperempuan.setSelected(false);
            } else {
                rblaki.setSelected(false); 
                rbperempuan.setSelected(true);
            }
            
            txttempat.setText(g);
            tgllahir.setDate(h);
            txtareaalamat.setText(i);
        
        } catch (ParseException ex) {
            Logger.getLogger(formadmintu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tableguruMouseClicked

